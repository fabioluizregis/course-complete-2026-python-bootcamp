table_of_5 = {num: num * 5 for num in range(1, 11)}
print(table_of_5)  # Output: {1: 5, 2: 10, 3: 15, 4: 20, 5: 25, 6: 30, 7: 35, 8: 40, 9: 45, 10: 50} 

squared_numbers = {num: num ** 2 for num in range(1, 11)}
print(squared_numbers)  # Output: {1: 1, 2: 4, 3: 9, 4: 16, 5: 25, 6: 36, 7: 49, 8: 64, 9: 81, 10: 100}

