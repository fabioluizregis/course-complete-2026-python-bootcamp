marks = [5, 2, 21, 5, 7]
extra_marks = [10, 15, 25]

print(marks)

marks.append(95)  # Adding an element at the end
print(marks)

marks.pop()  # Removing the last element
print(marks)

marks.extend(extra_marks) # Extending the list with another list
print(marks)  

marks.sort()  # Sorting the list
print(marks)